    def aggressive_correction(self, pred, m14b, m14a, prev_pred=None):
        """ê³µê²©ì ì¸ ë³´ì • í•¨ìˆ˜"""
        original_pred = pred
        
        # 1. M14B ê¸°ë°˜ ìµœê°• ë³´ì •
        if m14b >= 500:
            # ê·¹í•œê°’
            pred = max(1750, pred * 1.30)
            
        elif m14b >= 450:
            # ë¬´ì¡°ê±´ ìœ„í—˜êµ¬ê°„ ìƒìœ„
            pred = max(1720, pred * 1.25)
            
        elif m14b >= 400:
            # ìœ„í—˜êµ¬ê°„ ì§„ì… ë³´ì¥
            pred = max(1700, pred * 1.20)
            
        elif m14b >= 350:
            # ì‹ í˜¸êµ¬ê°„ ìƒìœ„
            pred = max(1670, pred * 1.15)
            
        elif m14b >= 300:
            # ì‹ í˜¸êµ¬ê°„ ì§„ì…
            pred = max(1650, pred * 1.10)
            
        # 2. í™©ê¸ˆ íŒ¨í„´ íŠ¹ë³„ ë³´ì •
        if m14b > 300 and m14a < 80:
            ratio = m14b / (m14a + 1)
            if ratio > 5:
                pred = max(1700, pred * 1.18)
            elif ratio > 4:
                pred = max(1680, pred * 1.15)
            else:
                pred = max(1660, pred * 1.12)
        
        # 3. ì´ì „ê°’ ì—°ì†ì„±
        if prev_pred is not None and prev_pred >= 1680:
            if m14b >= 350:  # M14Bê°€ ë†’ìœ¼ë©´ ìœ ì§€
                pred = max(prev_pred * 0.95, pred)
        
        # 4. ìµœì¢… êµ¬ê°„ë³„ ìµœì†Œê°’ ë³´ì¥
        if pred >= 1650:
            pred = max(pred, 1680)  # ì‹ í˜¸êµ¬ê°„ì€ ìµœì†Œ 1680
        if pred >= 1700:
            pred = max(pred, 1730)  # ìœ„í—˜êµ¬ê°„ì€ ìµœì†Œ 1730
            
        return pred
    
