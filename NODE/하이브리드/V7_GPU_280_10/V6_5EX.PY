def weighted_mae(y_true, y_pred):
    """ìœ„í—˜êµ¬ê°„ì— ë” í° ê°€ì¤‘ì¹˜ë¥¼ ë¶€ì—¬í•˜ëŠ” MAE"""
    # y_trueëŠ” ì´ë¯¸ 0-1 ì‚¬ì´ë¡œ ìŠ¤ì¼€ì¼ë§ëœ ê°’ì…ë‹ˆë‹¤.
    # ìœ„í—˜êµ¬ê°„(1651~1682)ì˜ ìŠ¤ì¼€ì¼ë§ëœ ë²”ìœ„ë¥¼ ê³„ì‚°í•©ë‹ˆë‹¤.
    # ì´ ê³„ì‚°ì€ scaler_yê°€ í•™ìŠµëœ í›„ì—ì•¼ ì •í™•í•˜ì§€ë§Œ, ì—¬ê¸°ì„œëŠ” ê·¼ì‚¬ì¹˜ë¥¼ ì‚¬ìš©í•©ë‹ˆë‹¤.
    # ì˜ˆë¥¼ ë“¤ì–´, ë°ì´í„° ë²”ìœ„ê°€ [800, 2000]ì´ë¼ë©´,
    # (1651 - 800) / (2000 - 800) = 0.709
    # (1682 - 800) / (2000 - 800) = 0.735
    # ë°ì´í„° ë¶„í¬ì— ë”°ë¼ ë‹¬ë¼ì§€ë¯€ë¡œ, ì—¬ê¸°ì„œëŠ” ëŒ€ëµì ì¸ ê°’ì„ ì‚¬ìš©í•©ë‹ˆë‹¤.
    danger_min_scaled = 0.7 
    danger_max_scaled = 0.75
    
    # ê°€ì¤‘ì¹˜ í…ì„œ ìƒì„± (ê¸°ë³¸ê°’ 1.0)
    weights = tf.ones_like(y_true)
    
    # ìœ„í—˜êµ¬ê°„ ë§ˆìŠ¤í¬ ìƒì„±
    danger_mask = tf.logical_and(
        y_true >= danger_min_scaled,
        y_true <= danger_max_scaled
    )
    
    # ìœ„í—˜êµ¬ê°„ì— í•´ë‹¹í•˜ëŠ” ìƒ˜í”Œì— 3ë°°ì˜ ê°€ì¤‘ì¹˜ ë¶€ì—¬
    weights = tf.where(danger_mask, 3.0, weights)
    
    # ê°€ì¤‘ì¹˜ê°€ ì ìš©ëœ MAE ê³„ì‚°
    mae = tf.abs(y_true - y_pred)
    weighted_mae_val = mae * weights
    
    return tf.reduce_mean(weighted_mae_val)

