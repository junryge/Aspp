        # ìŠ¤ì¼€ì¼ë§
        print("\nâš™ï¸ ìŠ¤ì¼€ì¼ë§...")
        
        # X ìŠ¤ì¼€ì¼ë§
        n_samples, n_timesteps, n_features = X_train.shape
        X_train_2d = X_train.reshape(-1, n_features)
