def weighted_mae(y_true, y_pred):
    """ÏúÑÌóòÍµ¨Í∞ÑÏóê Îçî ÌÅ∞ Í∞ÄÏ§ëÏπò"""
    # Ïä§ÏºÄÏùºÎêú Í∞íÏùÑ ÏõêÎ≥∏ Î≤îÏúÑÎ°ú ÎåÄÎûµ Î≥ÄÌôò
    y_true_denorm = y_true * 900 + 900  # MinMaxScaler Ïó≠Î≥ÄÌôò Ï∂îÏ†ï
    
    # Í∞ÄÏ§ëÏπò Í≥ÑÏÇ∞
    weights = tf.ones_like(y_true)
    
    # ÏúÑÌóòÍµ¨Í∞Ñ ÎßàÏä§ÌÅ¨ (1651~1682Î•º Ïä§ÏºÄÏùºÎêú Í∞íÏúºÎ°ú Î≥ÄÌôò)
    danger_min_scaled = (1651 - 900) / 900
    danger_max_scaled = (1682 - 900) / 900
    
    danger_mask = tf.logical_and(
        y_true >= danger_min_scaled,
        y_true <= danger_max_scaled
    )
    
    # ÏúÑÌóòÍµ¨Í∞ÑÏóê 3Î∞∞ Í∞ÄÏ§ëÏπò
    weights = tf.where(danger_mask, 3.0, weights)
    
    # Weighted MAE
    mae = tf.abs(y_true - y_pred)
    weighted_mae = mae * weights
    
    return tf.reduce_mean(weighted_mae)

