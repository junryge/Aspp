def build_extreme_net(input_shape):
    """
    EXTREME_NET - LSTM + Multi-Head Attention ìœµí•©
    íŠ¹ì§•: ì¥ê¸° íŒ¨í„´(LSTM) + ì¤‘ìš” ì‹œì  í¬ì°©(Attention)
    """
    inputs = Input(shape=input_shape, name='extreme_inputs')
    
    # LSTM ë¸Œëœì¹˜ (ì‹œê³„ì—´ íŒ¨í„´ í•™ìŠµ)
    lstm = LSTM(48, return_sequences=True, name='lstm1')(inputs)
    lstm = LSTM(24, name='lstm2')(lstm)
    
    # Attention ë¸Œëœì¹˜ (ì¤‘ìš” ì‹œì  í¬ì°©)
    attn = MultiHeadAttention(
        num_heads=4, 
        key_dim=12,
        name='multi_head_attention'
    )(inputs, inputs)
    attn = GlobalAveragePooling1D(name='global_avg_pool')(attn)
    
    # ìœµí•©
    x = Concatenate(name='fusion')([lstm, attn])
    x = BatchNormalization(name='batch_norm')(x)
    x = Dense(48, activation='relu', name='dense1')(x)
    x = Dropout(0.2, name='dropout')(x)
    
    # ì¶œë ¥ì¸µ
    out_reg = Dense(1, name='regression')(x)  # íšŒê·€ (ë¬¼ë¥˜ëŸ‰ ì˜ˆì¸¡)
    out_cls = Dense(3, activation='softmax', name='classification')(x)  # ë¶„ë¥˜ (êµ¬ê°„)
    
    model = Model(inputs, [out_reg, out_cls], name='ExtremeNet')
    
    print("\nğŸ“Š EXTREME_NET êµ¬ì¡°:")
    print(f"  - LSTM ë ˆì´ì–´: 48 â†’ 24")
    print(f"  - Attention Heads: 4ê°œ (key_dim=12)")
    print(f"  - ìœµí•© Dense: 48")
    print(f"  - Dropout: 20%")
    print(f"  - ì¶œë ¥: íšŒê·€ + 3êµ¬ê°„ ë¶„ë¥˜")
    
    return model

