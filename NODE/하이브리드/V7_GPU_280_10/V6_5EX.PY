class DataProcessor:
    def __init__(self):
        self.seq_len = 100
        self.pred_len = 10
        
        # ìŠ¤ì¼€ì¼ëŸ¬
        self.scaler_X = StandardScaler()
        self.scaler_y = MinMaxScaler(feature_range=(0, 1))
        
        # â­ êµ¬ê°„ ì¬ì •ì˜
        self.NORMAL_MAX = 1400      # ì •ìƒ ìƒí•œ
        self.CAUTION_MAX = 1650     # ì£¼ì˜ ìƒí•œ
        self.SIGNAL_MIN = 1651      # ì‹ í˜¸ ì‹œì‘ (1700+ ì „ì¡°)
        self.SIGNAL_MAX = 1699      # ì‹ í˜¸ ë
        self.DANGER_MIN = 1700      # ìœ„í—˜ ì‹œì‘
        
        self.feature_columns = None
        
    def load_data(self, filepath):
        """ë°ì´í„° ë¡œë“œ"""
        print(f"\nğŸ“‚ ë°ì´í„° ë¡œë”©: {filepath}")
        df = pd.read_csv(filepath)
        
        # ì‹œê°„ ì •ë ¬
        df['CURRTIME'] = pd.to_datetime(df['CURRTIME'].astype(str), 
                                       format='%Y%m%d%H%M', errors='coerce')
        df = df.sort_values('CURRTIME').reset_index(drop=True)
        
        # 0ê°’ ì œê±°
        df = df[df['TOTALCNT'] > 0].reset_index(drop=True)
        print(f"âœ… ë°ì´í„°: {len(df):,}ê°œ")
        
        # êµ¬ê°„ í†µê³„ (ì¬ì •ì˜)
        normal = (df['TOTALCNT'] < self.NORMAL_MAX).sum()
        caution = ((df['TOTALCNT'] >= self.NORMAL_MAX) & 
                  (df['TOTALCNT'] <= self.CAUTION_MAX)).sum()
        signal = ((df['TOTALCNT'] >= self.SIGNAL_MIN) & 
                 (df['TOTALCNT'] <= self.SIGNAL_MAX)).sum()
        danger = (df['TOTALCNT'] >= self.DANGER_MIN).sum()
        
        print(f"\nğŸ“Š TOTALCNT ë¶„í¬ (ì¬ì •ì˜):")
        print(f"  ğŸŸ¢ ì •ìƒ(~1400): {normal:,}ê°œ ({normal/len(df)*100:.1f}%)")
        print(f"  ğŸŸ¡ ì£¼ì˜(1400~1650): {caution:,}ê°œ ({caution/len(df)*100:.1f}%)")
        print(f"  ğŸŸ  ì‹ í˜¸(1651~1699): {signal:,}ê°œ ({signal/len(df)*100:.1f}%) â† 1700+ ì „ì¡°!")
        print(f"  ğŸ”´ ìœ„í—˜(1700+): {danger:,}ê°œ ({danger/len(df)*100:.1f}%)")
        
        return df
    
    def create_features(self, df):
        """íŠ¹ì„± ìƒì„± (ì‹ í˜¸êµ¬ê°„ ê°•ì¡°)"""
        print("\nâš™ï¸ íŠ¹ì„± ìƒì„± ì¤‘...")
        
        # ê¸°ë³¸ íŠ¹ì„±
        df['RATIO'] = df['M14AM14B'] / (df['M14AM10A'] + 1)
        df['GOLDEN'] = ((df['M14AM14B'] > 300) & (df['M14AM10A'] < 80)).astype(int)
        
        # â­ ì‹ í˜¸êµ¬ê°„ íŠ¹ì„± ê°•ì¡°
        df['SIGNAL_ZONE'] = ((df['TOTALCNT'] >= self.SIGNAL_MIN) & 
                             (df['TOTALCNT'] <= self.SIGNAL_MAX)).astype(int)
        df['PRE_SIGNAL'] = ((df['TOTALCNT'] >= 1600) & 
                            (df['TOTALCNT'] < self.SIGNAL_MIN)).astype(int)
        df['DANGER_ZONE'] = (df['TOTALCNT'] >= self.DANGER_MIN).astype(int)
        
        # ì´ë™í‰ê· 
        for w in [5, 10, 20]:
            df[f'MA_{w}'] = df['TOTALCNT'].rolling(w, min_periods=1).mean()
            df[f'STD_{w}'] = df['TOTALCNT'].rolling(w, min_periods=1).std().fillna(0)
            df[f'M14B_MA_{w}'] = df['M14AM14B'].rolling(w, min_periods=1).mean()
        
        # ë³€í™”ìœ¨
        for lag in [1, 5, 10]:
            df[f'CHANGE_{lag}'] = df['TOTALCNT'].diff(lag).fillna(0)
            df[f'M14B_CHANGE_{lag}'] = df['M14AM14B'].diff(lag).fillna(0)
        
        # ì—°ì† ìƒìŠ¹
        df['RISE'] = (df['TOTALCNT'] > df['TOTALCNT'].shift(1)).astype(int)
        df['RISE_COUNT'] = df['RISE'].rolling(10, min_periods=1).sum()
        
        # íŒ¨í„´ (ì‹ í˜¸êµ¬ê°„ í¬í•¨)
        df['PATTERN'] = 0
        signal_mask = ((df['TOTALCNT'] >= self.SIGNAL_MIN) & 
                      (df['TOTALCNT'] <= self.SIGNAL_MAX))
        high_m14b = df['M14AM14B'].rolling(100, min_periods=1).mean() > 380
        
        df.loc[signal_mask, 'PATTERN'] = 1  # UU1: ì‹ í˜¸
        df.loc[high_m14b, 'PATTERN'] = 2     # UU2: ê³ ìœ„í—˜
        
        # ì¶”ì„¸
        df['TREND'] = 0
        for i in range(20, len(df)):
            recent = df['TOTALCNT'].iloc[i-20:i].values
            if len(recent) > 1:
                slope = np.polyfit(range(len(recent)), recent, 1)[0]
                if slope > 5:
                    df.loc[i, 'TREND'] = 2  # ìƒìŠ¹
                elif slope < -5:
                    df.loc[i, 'TREND'] = 0  # í•˜ë½
                else:
                    df.loc[i, 'TREND'] = 1  # ë³´í•©
        
        print(f"âœ… íŠ¹ì„± ìƒì„± ì™„ë£Œ")
        return df
    
    def create_sequences(self, df):
        """ì‹œí€€ìŠ¤ ìƒì„± (ìƒìŠ¹ ì¶”ì„¸ ì‹ í˜¸êµ¬ê°„ë§Œ ì˜¤ë²„ìƒ˜í”Œë§)"""
        print("\nğŸ”„ ì‹œí€€ìŠ¤ ìƒì„± ì¤‘...")
        
        # ì‚¬ìš©í•  ì»¬ëŸ¼
        self.feature_columns = [
            'TOTALCNT', 'M14AM14B', 'M14AM14BSUM', 'M14AM10A', 'M14AM16',
            'RATIO', 'GOLDEN', 
            'SIGNAL_ZONE', 'PRE_SIGNAL', 'DANGER_ZONE',  # ì‹ í˜¸êµ¬ê°„ íŠ¹ì„±
            'PATTERN', 'TREND',
            'MA_5', 'MA_10', 'MA_20',
            'STD_5', 'STD_10', 'STD_20',
            'M14B_MA_5', 'M14B_MA_10', 'M14B_MA_20',
            'CHANGE_1', 'CHANGE_5', 'CHANGE_10',
            'M14B_CHANGE_1', 'M14B_CHANGE_5', 'M14B_CHANGE_10',
            'RISE_COUNT'
        ]
        
        print(f"  íŠ¹ì„±: {len(self.feature_columns)}ê°œ")
        
        # ë°ì´í„° ì¤€ë¹„
        X_data = df[self.feature_columns].values
        y_data = df['TOTALCNT'].shift(-self.pred_len).values
        
        # ì‹œí€€ìŠ¤ ìƒì„±
        X, y = [], []
        signal_X, signal_y = [], []  # ì‹ í˜¸êµ¬ê°„ ë”°ë¡œ ì €ì¥
        
        for i in range(len(df) - self.seq_len - self.pred_len):
            X.append(X_data[i:i+self.seq_len])
            target = y_data[i+self.seq_len-1]
            current = df['TOTALCNT'].iloc[i+self.seq_len-1]
            y.append(target)
            
            # â­ ì¤‘ìš”: ìƒìŠ¹ ì¶”ì„¸ì¸ì§€ í™•ì¸ (í˜„ì¬ < ë¯¸ë˜)
            is_rising = target > current  # 10ë¶„ í›„ê°€ í˜„ì¬ë³´ë‹¤ ë†’ìœ¼ë©´ ìƒìŠ¹
            
            # ìƒìŠ¹ ì¶”ì„¸ì¼ ë•Œë§Œ ìœ„í—˜ ì‹ í˜¸ë¡œ ì²˜ë¦¬
            if is_rising:
                # ì‹ í˜¸êµ¬ê°„(1651~1699) + ìƒìŠ¹ â†’ ì˜¤ë²„ìƒ˜í”Œë§ 5ë°°
                if self.SIGNAL_MIN <= target <= self.SIGNAL_MAX:
                    for _ in range(5):
                        signal_X.append(X_data[i:i+self.seq_len])
                        signal_y.append(target)
                # ìœ„í—˜êµ¬ê°„(1700+) + ìƒìŠ¹ â†’ ì˜¤ë²„ìƒ˜í”Œë§ 10ë°°
                elif target >= self.DANGER_MIN:
                    for _ in range(10):
                        signal_X.append(X_data[i:i+self.seq_len])
                        signal_y.append(target)
        
        # ì˜¤ë²„ìƒ˜í”Œë§ ë°ì´í„° ì¶”ê°€
        if signal_X:
            X.extend(signal_X)
            y.extend(signal_y)
            print(f"  ğŸ”¥ ìƒìŠ¹ ì¶”ì„¸ ì‹ í˜¸/ìœ„í—˜êµ¬ê°„ {len(signal_X)}ê°œ ì¶”ê°€ (ì˜¤ë²„ìƒ˜í”Œë§)")
        
        X = np.array(X, dtype=np.float32)
        y = np.array(y, dtype=np.float32)
        
        # NaN ì œê±°
        valid_idx = ~np.isnan(y)
        X = X[valid_idx]
        y = y[valid_idx]
        
        print(f"  ì´ ì‹œí€€ìŠ¤: {X.shape[0]}ê°œ")
        
        # ì…”í”Œ
        shuffle_idx = np.arange(len(X))
