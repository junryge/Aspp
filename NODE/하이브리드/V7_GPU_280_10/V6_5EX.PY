    # ì²´í¬í¬ì¸íŠ¸ ê´€ë¦¬ì
    cp_manager = CheckpointManager()
    model, start_epoch = cp_manager.load_checkpoint()
    
    # ë¶„ì‚° í•™ìŠµ ì „ëµ ë²”ìœ„ ë‚´ì—ì„œ ëª¨ë¸ ìƒì„± ë° ì»´íŒŒì¼
