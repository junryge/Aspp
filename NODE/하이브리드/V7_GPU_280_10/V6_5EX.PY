            if np.max(totalcnt) > 1682:
                uu2_score += 0.5
            
            # UU1 ê°€ëŠ¥ì„± ì ìˆ˜
            uu1_score = 1 - uu2_score
            
            # ê¸‰ì¦ ìœ„í—˜ë„
            spike_risk = 0
