def main():
    try:
        # í‰ê°€ê¸° ìƒì„±
        evaluator = DataEvaluator()
        
        # ë°ì´í„° ê²½ë¡œ ì°¾ê¸°
        data_paths = [
            'data/20250732_to20250806.csv',
            '20250732_to20250806.csv', 
            'data/20250807_DATA.CSV',
            '20250807_DATA.CSV',
            'data/20240201_TO_202507281705.csv',
            '20240201_TO_202507281705.csv'
        ]
        
        data_path = None
        for path in data_paths:
            if os.path.exists(path):
                data_path = path
                print(f"ğŸ“Œ ë°ì´í„°: {data_path}")
                break
        
        if not data_path:
            print("âŒ ë°ì´í„° íŒŒì¼ ì—†ìŒ!")
            return None
        
        # ë°ì´í„° ì²˜ë¦¬
        df = evaluator.load_and_process_data(data_path)
        df = evaluator.create_features(df)
        
        # ì˜ˆì¸¡
        results_df = evaluator.predict_sequences(df)
        
        if results_df.empty:
            print("âš ï¸ ì˜ˆì¸¡ ê²°ê³¼ ì—†ìŒ")
            return results_df
        
        # í‰ê°€
        evaluator.evaluate_results(results_df)
        
        # ì €ì¥
        output_file = 'prediction_results.csv'
        save_columns = ['DateTime', 'TargetDateTime', 'DailyMax', 'ActualValue', 
                       'PredictedValue', 'Error'] if 'ActualValue' in results_df.columns \
                      else ['DateTime', 'TargetDateTime', 'DailyMax', 'PredictedValue']
        
        results_df[save_columns].to_csv(output_file, index=False, encoding='utf-8-sig')
        print(f"\nğŸ’¾ ì €ì¥: {output_file}")
        
        # ìƒ˜í”Œ ì¶œë ¥
        print("\nğŸ“‹ ê²°ê³¼ ìƒ˜í”Œ:")
        print(results_df[save_columns].head(10))
        
        # í†µê³„
        print(f"\nğŸ“Š ì˜ˆì¸¡ê°’ ìš”ì•½:")
        print(f"  í‰ê· : {results_df['PredictedValue'].mean():.2f}")
        print(f"  í‘œì¤€í¸ì°¨: {results_df['PredictedValue'].std():.2f}")
        print(f"  ìµœì†Œ: {results_df['PredictedValue'].min():.2f}")
        print(f"  ìµœëŒ€: {results_df['PredictedValue'].max():.2f}")
        
        print("\nâœ… ì™„ë£Œ!")
        return results_df
        
    except Exception as e:
        print(f"\nâŒ ì˜¤ë¥˜: {e}")
        import traceback
        traceback.print_exc()
        return None

if __name__ == "__main__":
    results = main()