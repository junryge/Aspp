import numpy as np
import pandas as pd
import tensorflow as tf
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
import pickle
import json
import os
import warnings
from datetime import datetime, timedelta
import matplotlib.pyplot as plt

warnings.filterwarnings('ignore')
tf.keras.config.enable_unsafe_deserialization()

class CompleteModelEvaluator:
    def __init__(self, scaler_path='scalers/'):
        """í‰ê°€ê¸° ì´ˆê¸°í™”"""
        print("="*80)
        print("ğŸ“Š ì „ì²´ ëª¨ë¸ ì˜ˆì¸¡ í‰ê°€ ì‹œìŠ¤í…œ")
        print("="*80)
        
        # ìŠ¤ì¼€ì¼ëŸ¬ ë¡œë“œ
        with open(f'{scaler_path}feature_scaler.pkl', 'rb') as f:
            self.feature_scaler = pickle.load(f)
        with open(f'{scaler_path}target_scaler.pkl', 'rb') as f:
            self.target_scaler = pickle.load(f)
        with open(f'{scaler_path}config.json', 'r') as f:
            config = json.load(f)
            self.seq_len = config['seq_len']
            self.pred_len = config['pred_len']
            self.feature_columns = config['feature_columns']
        print(f"âœ… ìŠ¤ì¼€ì¼ëŸ¬ ë¡œë“œ ì™„ë£Œ")
        
        self.models = {}
        
    def load_all_models(self, model_dir='models/'):
        """ëª¨ë“  ëª¨ë¸ ë¡œë“œ"""
        print(f"\nğŸ“ ëª¨ë¸ ë¡œë”©...")
        
        model_files = [f for f in os.listdir(model_dir) if f.endswith('.keras')]
        
        for model_file in model_files:
            model_name = model_file.replace('.keras', '')
            model_path = os.path.join(model_dir, model_file)
            
            try:
                self.models[model_name] = tf.keras.models.load_model(
                    model_path, safe_mode=False
                )
                print(f"  âœ… {model_name} ë¡œë“œ ì™„ë£Œ")
            except Exception as e:
                print(f"  âŒ {model_name} ë¡œë“œ ì‹¤íŒ¨: {e}")
        
        print(f"\nì´ {len(self.models)}ê°œ ëª¨ë¸ ë¡œë“œ ì™„ë£Œ")
        return self.models
    
    def load_test_data(self, filepath):
        """í…ŒìŠ¤íŠ¸ ë°ì´í„° ë¡œë“œ"""
        print(f"\nğŸ“‚ í‰ê°€ ë°ì´í„° ë¡œë”©: {filepath}")
        df = pd.read_csv(filepath)
        print(f"  ì›ë³¸: {df.shape[0]:,}í–‰")
        
        # 0ê°’ ì œê±°
        df = df[df['TOTALCNT'] > 0].reset_index(drop=True)
        
        # ì‹œê°„ ë³€í™˜
        df['CURRTIME'] = pd.to_datetime(df['CURRTIME'].astype(str), 
                                       format='%Y%m%d%H%M', errors='coerce')
        df = df.sort_values('CURRTIME').reset_index(drop=True)
        
        print(f"  ìœ íš¨: {df.shape[0]:,}í–‰")
        return df
    
    def create_features(self, df):
        """íŠ¹ì„± ìƒì„±"""
        df['RATIO'] = df['M14AM14B'] / (df['M14AM10A'] + 1)
        df['GOLDEN'] = ((df['M14AM14B'] > 300) & (df['M14AM10A'] < 80)).astype(float)
        
        df['HOUR'] = df['CURRTIME'].dt.hour
        df['HOUR_SIN'] = np.sin(2 * np.pi * df['HOUR'] / 24)
        df['HOUR_COS'] = np.cos(2 * np.pi * df['HOUR'] / 24)
        
        for w in [10, 30]:
            df[f'MA_{w}'] = df['TOTALCNT'].rolling(w, min_periods=1).mean()
            df[f'STD_{w}'] = df['TOTALCNT'].rolling(w, min_periods=1).std().fillna(0)
        
        df['CHANGE_1'] = df['TOTALCNT'].diff(1).fillna(0)
        df['CHANGE_10'] = df['TOTALCNT'].diff(10).fillna(0)
        
        return df
    
    def evaluate_all_models(self, test_file):
        """ëª¨ë“  ëª¨ë¸ í‰ê°€ ë° ì˜ˆì¸¡ê°’ ì €ì¥"""
        
        # ë°ì´í„° ë¡œë“œ
        df = self.load_test_data(test_file)
        df = self.create_features(df)
        
        # ì˜ˆì¸¡ ê°€ëŠ¥ ë²”ìœ„
        start_idx = self.seq_len
        end_idx = len(df) - self.pred_len
        total = end_idx - start_idx
        
        print(f"\nğŸ”® ì˜ˆì¸¡ ì‹œì‘...")
        print(f"  ì‹œí€€ìŠ¤: {self.seq_len}ë¶„ â†’ ì˜ˆì¸¡: {self.pred_len}ë¶„ í›„")
        print(f"  ì˜ˆì¸¡ ê°œìˆ˜: {total:,}ê°œ")
        
        # ëª¨ë“  ì˜ˆì¸¡ì„ ì €ì¥í•  DataFrame ì¤€ë¹„
        all_predictions = pd.DataFrame()
        
        # ì‹œê°„ ì •ë³´ ìˆ˜ì§‘
        timestamps_pred = []  # ì˜ˆì¸¡ ì‹œì 
        timestamps_target = []  # ì˜ˆì¸¡ ëŒ€ìƒ ì‹œê°„
        actuals = []  # ì‹¤ì œê°’
        
        print("\nğŸ“Š ì‹œê°„ ì •ë³´ ìˆ˜ì§‘ ì¤‘...")
        for i in range(start_idx, end_idx):
            pred_time = df.iloc[i]['CURRTIME']
            target_time = pred_time + timedelta(minutes=self.pred_len)
            
            actual_idx = i + self.pred_len
            if actual_idx < len(df):
                timestamps_pred.append(pred_time)
                timestamps_target.append(target_time)
                actuals.append(df.iloc[actual_idx]['TOTALCNT'])
        
        # ê¸°ë³¸ ì •ë³´ ì €ì¥
        all_predictions['ì˜ˆì¸¡ì‹œì '] = [t.strftime('%Y-%m-%d %H:%M') for t in timestamps_pred]
        all_predictions['ì˜ˆì¸¡ëŒ€ìƒì‹œê°„'] = [t.strftime('%Y-%m-%d %H:%M') for t in timestamps_target]
        all_predictions['ì‹¤ì œê°’'] = actuals
        
        print(f"  ì˜ˆì¸¡í•  ë°ì´í„°: {len(all_predictions)}ê°œ")
        
        # ê° ëª¨ë¸ë³„ ì˜ˆì¸¡
        model_metrics = {}
        
        for model_name, model in self.models.items():
            print(f"\nğŸ¯ {model_name} ì˜ˆì¸¡ ì¤‘...")
            predictions = []
            
            # ë°°ì¹˜ ì˜ˆì¸¡
            batch_size = 500
            for i in range(start_idx, end_idx, batch_size):
                batch_end = min(i + batch_size, end_idx)
                
                # ë°°ì¹˜ ë°ì´í„° ì¤€ë¹„
                X_batch = []
                for j in range(i, batch_end):
                    seq_data = df.iloc[j-self.seq_len:j][self.feature_columns].values
                    X_batch.append(seq_data)
                
                if len(X_batch) == 0:
                    continue
                
                # ìŠ¤ì¼€ì¼ë§
                X_batch = np.array(X_batch)
                X_batch_scaled = []
                for seq in X_batch:
                    seq_scaled = self.feature_scaler.transform(seq)
                    X_batch_scaled.append(seq_scaled)
                X_batch_scaled = np.array(X_batch_scaled)
                
                # ì˜ˆì¸¡
                preds = model.predict(X_batch_scaled, verbose=0)
                
                if isinstance(preds, list):
                    y_pred_scaled = preds[0].flatten()
                else:
                    y_pred_scaled = preds.flatten()
                
                # ì—­ë³€í™˜
                y_pred = self.target_scaler.inverse_transform(
                    y_pred_scaled.reshape(-1, 1)).flatten()
                
                # ìˆ˜ì§‘
                for k in range(len(y_pred)):
                    actual_idx = i - start_idx + k
                    if actual_idx < len(all_predictions):
                        predictions.append(y_pred[k])
                
                if len(predictions) % 2000 == 0:
                    print(f"    {len(predictions):,}/{len(all_predictions):,} ì™„ë£Œ")
            
            # ì˜ˆì¸¡ê°’ ì»¬ëŸ¼ ì¶”ê°€
            predictions = predictions[:len(all_predictions)]  # ê¸¸ì´ ë§ì¶”ê¸°
            all_predictions[f'{model_name}_ì˜ˆì¸¡'] = [round(p) for p in predictions]
            all_predictions[f'{model_name}_ì˜¤ì°¨'] = all_predictions[f'{model_name}_ì˜ˆì¸¡'] - all_predictions['ì‹¤ì œê°’']
            all_predictions[f'{model_name}_ì˜¤ì°¨ìœ¨(%)'] = round(
                abs(all_predictions[f'{model_name}_ì˜¤ì°¨']) / all_predictions['ì‹¤ì œê°’'] * 100, 2
            )
            
            # ì„±ëŠ¥ ê³„ì‚°
            mae = mean_absolute_error(all_predictions['ì‹¤ì œê°’'], predictions)
            rmse = np.sqrt(mean_squared_error(all_predictions['ì‹¤ì œê°’'], predictions))
            r2 = r2_score(all_predictions['ì‹¤ì œê°’'], predictions)
            mape = np.mean(abs(all_predictions[f'{model_name}_ì˜¤ì°¨']) / all_predictions['ì‹¤ì œê°’']) * 100
            
            model_metrics[model_name] = {
                'MAE': mae,
                'RMSE': rmse,
                'R2': r2,
                'MAPE': mape,
                'ì •í™•ë„(%)': 100 - mape
            }
            
            print(f"  âœ… {model_name} ì™„ë£Œ: MAE={mae:.2f}, RÂ²={r2:.4f}, ì •í™•ë„={100-mape:.2f}%")
        
        # ì•™ìƒë¸” ì˜ˆì¸¡ ì¶”ê°€
        print("\nğŸ”® ì•™ìƒë¸” ì˜ˆì¸¡ ìƒì„±...")
        
        # ì‚¬ìš© ê°€ëŠ¥í•œ ëª¨ë¸ë“¤ì˜ ì˜ˆì¸¡ê°’ìœ¼ë¡œ ì•™ìƒë¸”
        model_weights = {
            'PatchTST': 0.30,      # ìµœê³  ì„±ëŠ¥ 30%
            'ExtremeNet': 0.25,    # ë‘ë²ˆì§¸ 25%
            'StableLSTM': 0.20,    # ì„¸ë²ˆì§¸ 20%
            'SpikeDetector': 0.15, # ë„¤ë²ˆì§¸ 15%
            'GoldenRule': 0.10     # ë‹¤ì„¯ì§¸ 10%
        }
        
        # ì•™ìƒë¸” ê³„ì‚°
        ensemble_pred = np.zeros(len(all_predictions))
        total_weight = 0
        
        for model_name in self.models.keys():
            if model_name in model_weights:
                weight = model_weights[model_name]
                ensemble_pred += all_predictions[f'{model_name}_ì˜ˆì¸¡'] * weight
                total_weight += weight
            else:
                # ê°€ì¤‘ì¹˜ ì—†ëŠ” ëª¨ë¸ì€ ê· ë“± ê°€ì¤‘ì¹˜
                ensemble_pred += all_predictions[f'{model_name}_ì˜ˆì¸¡'] * 0.1
                total_weight += 0.1
        
        # ì •ê·œí™” (ê°€ì¤‘ì¹˜ í•©ì´ 1ì´ ë˜ë„ë¡)
        if total_weight > 0:
            ensemble_pred = ensemble_pred / total_weight
        
        # ì•™ìƒë¸” ê²°ê³¼ ì¶”ê°€
        all_predictions['ì•™ìƒë¸”_ì˜ˆì¸¡'] = [round(p) for p in ensemble_pred]
        all_predictions['ì•™ìƒë¸”_ì˜¤ì°¨'] = all_predictions['ì•™ìƒë¸”_ì˜ˆì¸¡'] - all_predictions['ì‹¤ì œê°’']
        all_predictions['ì•™ìƒë¸”_ì˜¤ì°¨ìœ¨(%)'] = round(
            abs(all_predictions['ì•™ìƒë¸”_ì˜¤ì°¨']) / all_predictions['ì‹¤ì œê°’'] * 100, 2
        )
        
        # ì•™ìƒë¸” ì„±ëŠ¥
        ensemble_mae = mean_absolute_error(all_predictions['ì‹¤ì œê°’'], ensemble_pred)
        ensemble_rmse = np.sqrt(mean_squared_error(all_predictions['ì‹¤ì œê°’'], ensemble_pred))
        ensemble_r2 = r2_score(all_predictions['ì‹¤ì œê°’'], ensemble_pred)
        ensemble_mape = np.mean(abs(all_predictions['ì•™ìƒë¸”_ì˜¤ì°¨']) / all_predictions['ì‹¤ì œê°’']) * 100
        
        model_metrics['ì•™ìƒë¸”'] = {
            'MAE': ensemble_mae,
            'RMSE': ensemble_rmse,
            'R2': ensemble_r2,
            'MAPE': ensemble_mape,
            'ì •í™•ë„(%)': 100 - ensemble_mape
        }
        
        print(f"\nâœ… ì•™ìƒë¸” ì™„ë£Œ: MAE={ensemble_mae:.2f}, RÂ²={ensemble_r2:.4f}, ì •í™•ë„={100-ensemble_mape:.2f}%")
        
        # CSV ì €ì¥
        output_file = f'all_models_predictions_{datetime.now().strftime("%Y%m%d_%H%M%S")}.csv'
        all_predictions.to_csv(output_file, index=False, encoding='utf-8-sig')
        print(f"\nğŸ’¾ ëª¨ë“  ëª¨ë¸ ì˜ˆì¸¡ê°’ ì €ì¥: {output_file}")
        
        # ì„±ëŠ¥ ìš”ì•½
        print("\n" + "="*80)
        print("ğŸ“Š ëª¨ë¸ ì„±ëŠ¥ ìš”ì•½")
        print("="*80)
        
        metrics_df = pd.DataFrame(model_metrics).T
        metrics_df = metrics_df.sort_values('R2', ascending=False)
        
        print(f"\n{'ëª¨ë¸':<15} {'MAE':>8} {'RMSE':>8} {'RÂ²':>8} {'MAPE(%)':>8} {'ì •í™•ë„(%)':>10}")
        print("-" * 65)
        
        for model_name, row in metrics_df.iterrows():
            if model_name == 'ì•™ìƒë¸”':
                # ì•™ìƒë¸” ê°•ì¡°
                print(f"{'ğŸ¯ ' + model_name:<15} {row['MAE']:8.2f} {row['RMSE']:8.2f} "
                      f"{row['R2']:8.4f} {row['MAPE']:8.2f} {row['ì •í™•ë„(%)']:10.2f} â­")
            else:
                print(f"{model_name:<15} {row['MAE']:8.2f} {row['RMSE']:8.2f} "
                      f"{row['R2']:8.4f} {row['MAPE']:8.2f} {row['ì •í™•ë„(%)']:10.2f}")
        
        # ì„±ëŠ¥ ë¹„êµ CSV ì €ì¥
        metrics_file = f'model_performance_{datetime.now().strftime("%Y%m%d_%H%M%S")}.csv'
        metrics_df.to_csv(metrics_file, encoding='utf-8-sig')
        print(f"\nğŸ’¾ ì„±ëŠ¥ ë¹„êµ ì €ì¥: {metrics_file}")
        
        # ìƒ˜í”Œ ì¶œë ¥
        print("\nğŸ“ ì˜ˆì¸¡ ìƒ˜í”Œ (ì²˜ìŒ 5ê°œ)")
        print("="*120)
        
        # ì»¬ëŸ¼ ì„ íƒ (ì•™ìƒë¸” í¬í•¨)
        display_cols = ['ì˜ˆì¸¡ì‹œì ', 'ì˜ˆì¸¡ëŒ€ìƒì‹œê°„', 'ì‹¤ì œê°’', 'ì•™ìƒë¸”_ì˜ˆì¸¡']
        for model_name in self.models.keys():
            display_cols.append(f'{model_name}_ì˜ˆì¸¡')
        
        # ì»¬ëŸ¼ì´ ì‹¤ì œë¡œ ì¡´ì¬í•˜ëŠ”ì§€ í™•ì¸
        display_cols = [col for col in display_cols if col in all_predictions.columns]
        
        print(all_predictions[display_cols].head())
        
        print("\nğŸ“ ì˜ˆì¸¡ ìƒ˜í”Œ (ë§ˆì§€ë§‰ 5ê°œ)")
        print("="*120)
        print(all_predictions[display_cols].tail())
        
        # ìµœê³  ëª¨ë¸ ê°•ì¡°
        best_model = metrics_df.index[0]
        print(f"\nğŸ† ìµœê³  ì„±ëŠ¥:")
        if best_model == 'ì•™ìƒë¸”':
            print(f"  ğŸ“Œ ì•™ìƒë¸” (5ê°œ ëª¨ë¸ ì¡°í•©)")
            print(f"  MAE: {metrics_df.loc[best_model, 'MAE']:.2f}")
            print(f"  RÂ²: {metrics_df.loc[best_model, 'R2']:.4f}")
            print(f"  ì •í™•ë„: {metrics_df.loc[best_model, 'ì •í™•ë„(%)']:.2f}%")
            print(f"\n  ì•™ìƒë¸” êµ¬ì„±:")
            print(f"    - PatchTST: 30%")
            print(f"    - ExtremeNet: 25%")
            print(f"    - StableLSTM: 20%")
            print(f"    - SpikeDetector: 15%")
            print(f"    - GoldenRule: 10%")
        else:
            print(f"  ğŸ“Œ {best_model}")
            print(f"  MAE: {metrics_df.loc[best_model, 'MAE']:.2f}")
            print(f"  RÂ²: {metrics_df.loc[best_model, 'R2']:.4f}")
            print(f"  ì •í™•ë„: {metrics_df.loc[best_model, 'ì •í™•ë„(%)']:.2f}%")
        
        # ê°„ë‹¨í•œ í†µê³„
        print("\nğŸ“Š ì˜ˆì¸¡ í†µê³„:")
        print(f"  ì´ ì˜ˆì¸¡ ê±´ìˆ˜: {len(all_predictions):,}ê°œ")
        print(f"  í‰ê°€ ê¸°ê°„: {all_predictions['ì˜ˆì¸¡ì‹œì '].iloc[0]} ~ {all_predictions['ì˜ˆì¸¡ì‹œì '].iloc[-1]}")
        
        return all_predictions, metrics_df

