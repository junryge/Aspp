            if model_name == 'ExtremeNet':
                print(f"    ExtremeNet ë¶€ìŠ¤íŒ… ì ìš©: {boost_applied_count}ê°œ "
                      f"({boost_applied_count/len(predictions)*100:.1f}%)")
            
