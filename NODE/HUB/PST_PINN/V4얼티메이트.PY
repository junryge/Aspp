def stable_loss(y_true, y_pred):
    """Model 1ìš© - ì•ˆì • êµ¬ê°„ íŠ¹í™” ì†ì‹¤ (í•¨ìˆ˜í˜•)"""
    # ì°¨ì› í™•ì¸ ë° ì¡°ì •
    y_true = tf.cast(y_true, tf.float32)
    y_pred = tf.cast(y_pred, tf.float32)
    
    # reshape to ensure same dimensions
    if len(y_true.shape) > 1:
        y_true = tf.squeeze(y_true)
    if len(y_pred.shape) > 1:
        y_pred = tf.squeeze(y_pred)
    
    error = tf.abs(y_true - y_pred)
    
    # False Positive ë°©ì§€
    penalty = tf.where(
        y_true < 300,
        tf.where(
            y_pred >= 300,
            error * 20.0,  # 300 ì´í•˜ë¥¼ 300+ ì˜ˆì¸¡: í° í˜ë„í‹°
            error * 1.0
        ),
        error * 0.5  # 300+ êµ¬ê°„ì€ ëœ ì¤‘ìš”
    )
    
    return penalty

def extreme_loss(y_true, y_pred):
    """Model 2ìš© - ê·¹ë‹¨ê°’ íŠ¹í™” ì†ì‹¤ (í•¨ìˆ˜í˜•)"""
    # ì°¨ì› í™•ì¸ ë° ì¡°ì •
    y_true = tf.cast(y_true, tf.float32)
    y_pred = tf.cast(y_pred, tf.float32)
    
    # reshape to ensure same dimensions
    if len(y_true.shape) > 1:
        y_true = tf.squeeze(y_true)
    if len(y_pred.shape) > 1:
        y_pred = tf.squeeze(y_pred)
    
    error = tf.abs(y_true - y_pred)
    
    # ê·¹ë‹¨ê°’ ë¯¸íƒì§€ ë°©ì§€
    penalty = tf.where(
        y_true >= 335,
        tf.where(
            y_pred < 335,
            error * 30.0,  # 335+ ë¯¸íƒì§€: ìµœëŒ€ í˜ë„í‹°
            error * 1.0
        ),
        tf.where(
            y_true >= 310,
            tf.where(
                y_pred < 310,
                error * 15.0,  # 310+ ë¯¸íƒì§€: í° í˜ë„í‹°
                error * 1.0
            ),
            tf.where(
                y_true >= 300,
                error * 5.0,  # 300-310 êµ¬ê°„: ì¤‘ìš”
                error * 0.3   # 300 ë¯¸ë§Œ: ëœ ì¤‘ìš”
            )
        )
    )
    
    return penalty

