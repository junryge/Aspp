    def analyze_data(self, df):
        """ë°ì´í„° ë¶„ì„ ë° ì í”„ ì¼€ì´ìŠ¤ íŒŒì•…"""
        target = df[self.target_col]
        
        print("\nğŸ“Š ë°ì´í„° ë¶„ì„:")
        print(f"  ë²”ìœ„: {target.min():.0f} ~ {target.max():.0f}")
        print(f"  í‰ê· : {target.mean():.1f}")
        print(f"  í‘œì¤€í¸ì°¨: {target.std():.1f}")
        
        # ì í”„ ì¼€ì´ìŠ¤ ë¶„ì„ (ë‹¤ì–‘í•œ ì •ì˜)
        jump_cases = []
        window = 20
        pred_len = 10
        
        for i in range(window, len(df) - pred_len):
            past_window = df[self.target_col].iloc[i-window:i]
            past_max = past_window.max()
            past_mean = past_window.mean()
            future_val = df[self.target_col].iloc[i+pred_len-1]
            
            # ì í”„ ì¡°ê±´ (v2, v3ë§Œ ì‚¬ìš©)
            # v2: í‰ê·  ëŒ€ë¹„ 35 ì´ìƒ ìƒìŠ¹
            # v3: ìµœëŒ€ê°’ ëŒ€ë¹„ 25 ì´ìƒ ìƒìŠ¹
            is_jump_v2 = (future_val - past_mean) > 35
            is_jump_v3 = (future_val - past_max) > 25
            
            if is_jump_v2 or is_jump_v3:
                jump_cases.append({
                    'index': i,
                    'past_max': past_max,
                    'past_mean': past_mean,
                    'future_val': future_val,
                    'jump_size': future_val - past_max,
                    'jump_from_mean': future_val - past_mean,
                    'type': 'v2' if is_jump_v2 else 'v3'
                })
        
        print(f"\nğŸš€ ì í”„ ì¼€ì´ìŠ¤ ë¶„ì„ (í™•ì¥ëœ ì •ì˜):")
        print(f"  ì´ ì í”„ ì¼€ì´ìŠ¤: {len(jump_cases)}ê°œ")
        if jump_cases:
            jump_df = pd.DataFrame(jump_cases)
            print(f"  í‰ê·  ì í”„ í¬ê¸°: {jump_df['jump_size'].mean():.1f}")
            print(f"  í‰ê· ì—ì„œ ì í”„: {jump_df['jump_from_mean'].mean():.1f}")
            
            # íƒ€ì…ë³„ ë¶„í¬
            type_counts = jump_df['type'].value_counts()
            for jump_type, count in type_counts.items():
                print(f"  íƒ€ì… {jump_type}: {count}ê°œ")
    
