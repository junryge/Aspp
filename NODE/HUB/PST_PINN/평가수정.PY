    """í‰ê°€ìš© ì‹œí€€ìŠ¤ ìƒì„±"""
    X_list = []
    y_actual_list = []
    y_time_list = []
    physics_list = []
    timestamps = []
    
    data = df[numeric_cols].values
    target_idx = numeric_cols.index(self.target_col)
    
    # ì‚¬ìš© ê°€ëŠ¥í•œ ì»¬ëŸ¼ í™•ì¸
    available_inflow = [col for col in self.inflow_cols if col in numeric_cols]
