    print(f"\nâŒ ì˜¤ì°¨ ìƒìœ„ 10ê°œ:")
    top_errors = results_df.nlargest(10, 'ì˜¤ì°¨ìœ¨(%)')
    print(top_errors[['í˜„ì¬ì‹œê°„', 'í˜„ì¬ê°’', 'ì‹¤ì œê°’', 'ì˜ˆì¸¡ê°’', 'ì˜¤ì°¨', 'ìœ„í—˜ë„', '300ê¸‰ì¦', 'ê¸‰ì¦ì„ë°•']].to_string(index=False))
   
    # ê¸‰ì¦ ì˜ˆì¸¡ ì‹¤íŒ¨ ì¼€ì´ìŠ¤
