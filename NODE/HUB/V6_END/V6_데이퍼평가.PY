import numpy as np
import pandas as pd
import pickle
from datetime import datetime, timedelta
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

def evaluate_and_save_csv(test_file='BBB.CSV'):
    """
    ì‹œê°„ê³¼ ì‹¤ì œê°’ ì •í™•íˆ ë§¤ì¹­í•˜ì—¬ í‰ê°€
    """
    # ëª¨ë¸ ë¡œë“œ
    try:
        with open('xgboost_model_30min_10min.pkl', 'rb') as f:
            model = pickle.load(f)
    except:
        print("âŒ ëª¨ë¸ íŒŒì¼ ì—†ìŒ")
        return
    
    # í…ŒìŠ¤íŠ¸ ë°ì´í„° ë¡œë“œ
    df_test = pd.read_csv(test_file, on_bad_lines='skip')
    TARGET_COL = 'CURRENT_M16A_3F_JOB_2'
    
    # STAT_DT ì²˜ë¦¬
    if 'STAT_DT' in df_test.columns:
        try:
            df_test['STAT_DT'] = pd.to_datetime(df_test['STAT_DT'].astype(str), format='%Y%m%d%H%M')
        except:
            base_date = datetime(2024, 1, 1)
            df_test['STAT_DT'] = [base_date + timedelta(minutes=i) for i in range(len(df_test))]
    
    results = []
    
    # 30ë¶„ ì‹œí€€ìŠ¤ë¡œ 10ë¶„ í›„ ì˜ˆì¸¡
    for i in range(30, len(df_test) - 10):
        # ì‹œí€€ìŠ¤ (i-30 ~ i-1)
        seq = df_test[TARGET_COL].iloc[i-30:i].values
        
        # ì‹œê°„ ì •ë³´
        seq_start_time = df_test['STAT_DT'].iloc[i-30]  # ì‹œí€€ìŠ¤ ì‹œì‘
        seq_end_time = df_test['STAT_DT'].iloc[i-1]     # ì‹œí€€ìŠ¤ ë
        current_time = df_test['STAT_DT'].iloc[i]       # í˜„ì¬ (ì˜ˆì¸¡ ì‹œì‘ì )
        target_time = df_test['STAT_DT'].iloc[i+9]      # 10ë¶„ í›„
        
        # ì‹¤ì œê°’: i ~ i+9 êµ¬ê°„ì˜ ìµœëŒ€ê°’
        actual_value = df_test[TARGET_COL].iloc[i:i+10].max()
        
        # Features
        features = pd.DataFrame([{
            'target_mean': np.mean(seq),
            'target_std': np.std(seq),
            'target_last_5_mean': np.mean(seq[-5:]),
            'target_max': np.max(seq),
            'target_min': np.min(seq),
            'target_slope': np.polyfit(np.arange(30), seq, 1)[0],
            'target_last_10_mean': np.mean(seq[-10:]),
            'target_first_10_mean': np.mean(seq[:10])
        }])
        
        # ì˜ˆì¸¡
        pred_value = model.predict(features)[0]
        
        # ê²°ê³¼ ì €ì¥
        results.append({
            'í˜„ì¬ì‹œê°„': current_time.strftime('%Y-%m-%d %H:%M'),
            'ì˜ˆì¸¡êµ¬ê°„ì‹œì‘': current_time.strftime('%Y-%m-%d %H:%M'),
            'ì˜ˆì¸¡êµ¬ê°„ì¢…ë£Œ': target_time.strftime('%Y-%m-%d %H:%M'),
            'ì‹œí€€ìŠ¤ì‹œì‘': seq_start_time.strftime('%Y-%m-%d %H:%M'),
            'ì‹œí€€ìŠ¤ì¢…ë£Œ': seq_end_time.strftime('%Y-%m-%d %H:%M'),
            'ì‹¤ì œê°’(10ë¶„MAX)': round(actual_value, 2),
            'ì˜ˆì¸¡ê°’': round(pred_value, 2),
            'ì˜¤ì°¨': round(abs(actual_value - pred_value), 2),
            'ì‹œí€€ìŠ¤MAX': round(np.max(seq), 2),
            'ì‹œí€€ìŠ¤MIN': round(np.min(seq), 2),
            'ì í”„ì¼€ì´ìŠ¤': 'O' if (np.max(seq) < 280 and actual_value >= 300) else '-',
            'ê·¹ë‹¨ê°’(300+)': 'O' if actual_value >= 300 else '-'
        })
    
    df_results = pd.DataFrame(results)
    
    # í‰ê°€ ì§€í‘œ
    actual = df_results['ì‹¤ì œê°’(10ë¶„MAX)'].values
    predicted = df_results['ì˜ˆì¸¡ê°’'].values
    
    mae = mean_absolute_error(actual, predicted)
    rmse = np.sqrt(mean_squared_error(actual, predicted))
    r2 = r2_score(actual, predicted)
    
    print(f"\nğŸ“Š í‰ê°€ ê²°ê³¼:")
    print(f"  MAE:  {mae:.2f}")
    print(f"  RMSE: {rmse:.2f}")
    print(f"  RÂ²:   {r2:.3f}")
    
    extreme_count = sum(actual >= 300)
    extreme_detected = sum((actual >= 300) & (predicted >= 290))
    
    print(f"\n  ê·¹ë‹¨ê°’: {extreme_count}ê°œ ì¤‘ {extreme_detected}ê°œ ê°ì§€")
    
    # CSV ì €ì¥
    df_results.to_csv('evaluation_results.csv', index=False, encoding='utf-8-sig')
    print(f"\nâœ… ì €ì¥: evaluation_results.csv")
    
    # ê²€ì¦ ì¶œë ¥ (ì²˜ìŒ 5ê°œ)
    print("\nê²€ì¦ (ì²˜ìŒ 5ê°œ):")
    for _, row in df_results.head(5).iterrows():
        print(f"{row['í˜„ì¬ì‹œê°„']} â†’ {row['ì˜ˆì¸¡êµ¬ê°„ì¢…ë£Œ']}: ì‹¤ì œ={row['ì‹¤ì œê°’(10ë¶„MAX)']}, ì˜ˆì¸¡={row['ì˜ˆì¸¡ê°’']}")
    
    return df_results

