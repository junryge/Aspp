def evaluate_and_save_csv(test_file='BBB.CSV'):
    """
    ì í”„ ì¼€ì´ìŠ¤ ì •í™•íˆ ê°ì§€
    """
    # ëª¨ë¸ ë¡œë“œ
    with open('xgboost_model_30min_10min.pkl', 'rb') as f:
        model = pickle.load(f)
    
    # ë°ì´í„° ë¡œë“œ
    df_test = pd.read_csv(test_file, on_bad_lines='skip')
    TARGET_COL = 'CURRENT_M16A_3F_JOB_2'
    
    # STAT_DT ì²˜ë¦¬
    if 'STAT_DT' in df_test.columns:
        df_test['STAT_DT'] = pd.to_datetime(df_test['STAT_DT'].astype(str), format='%Y%m%d%H%M')
    
    results = []
    jump_count = 0
    
    for i in range(30, len(df_test) - 10):
        seq = df_test[TARGET_COL].iloc[i-30:i].values
        
        current_time = df_test['STAT_DT'].iloc[i]
        seq_start = df_test['STAT_DT'].iloc[i-30]
        seq_end = df_test['STAT_DT'].iloc[i-1]
        prediction_time = df_test['STAT_DT'].iloc[i+9]
        
        # ì‹¤ì œê°’ë“¤
        actual_at_time = df_test[TARGET_COL].iloc[i+9]
        actual_max_10min = df_test[TARGET_COL].iloc[i:i+10].max()
        
        # Features
        features = pd.DataFrame([{
            'target_mean': np.mean(seq),
            'target_std': np.std(seq),
            'target_last_5_mean': np.mean(seq[-5:]),
            'target_max': np.max(seq),
            'target_min': np.min(seq),
            'target_slope': np.polyfit(np.arange(30), seq, 1)[0],
            'target_last_10_mean': np.mean(seq[-10:]),
            'target_first_10_mean': np.mean(seq[:10])
        }])
        
        pred_value = model.predict(features)[0]
        
        # ì í”„ ì¼€ì´ìŠ¤: ì‹œí€€ìŠ¤ MAX < 280 AND 10ë¶„ êµ¬ê°„ MAX >= 300
        seq_max = np.max(seq)
        is_jump = (seq_max < 280) and (actual_max_10min >= 300)
        
        if is_jump:
            jump_count += 1
            print(f"ğŸ”¥ ì í”„ ë°œê²¬! ì‹œí€€ìŠ¤MAX: {seq_max:.0f} â†’ ì‹¤ì œMAX: {actual_max_10min:.0f}")
        
        results.append({
            'í˜„ì¬ì‹œê°„': current_time.strftime('%Y-%m-%d %H:%M'),
            'ì˜ˆì¸¡ì‹œê°„(+10ë¶„)': prediction_time.strftime('%Y-%m-%d %H:%M'),
            'ì‹œí€€ìŠ¤ì‹œì‘': seq_start.strftime('%Y-%m-%d %H:%M'),
            'ì‹œí€€ìŠ¤ì™„ë£Œ': seq_end.strftime('%Y-%m-%d %H:%M'),
            'ì‹¤ì œê°’': round(actual_at_time, 2),
            '10ë¶„êµ¬ê°„MAX': round(actual_max_10min, 2),
            'ì˜ˆì¸¡ê°’': round(pred_value, 2),
            'ì˜¤ì°¨': round(abs(actual_max_10min - pred_value), 2),
            'ì‹œí€€ìŠ¤MAX': round(seq_max, 2),
            'ì‹œí€€ìŠ¤MIN': round(np.min(seq), 2),
            'ì í”„ì¼€ì´ìŠ¤': 'O' if is_jump else '-',
            'ê·¹ë‹¨ê°’(300+)': 'O' if actual_max_10min >= 300 else '-'
        })
    
    df_results = pd.DataFrame(results)
    
    print(f"\nì´ ì í”„ ì¼€ì´ìŠ¤: {jump_count}ê°œ")
    
    # ì í”„ ì¼€ì´ìŠ¤ë§Œ ì¶œë ¥
    if jump_count > 0:
        jump_df = df_results[df_results['ì í”„ì¼€ì´ìŠ¤'] == 'O']
        print("\nì í”„ ì¼€ì´ìŠ¤ ìƒì„¸:")
        print(jump_df[['í˜„ì¬ì‹œê°„', 'ì‹œí€€ìŠ¤MAX', '10ë¶„êµ¬ê°„MAX', 'ì˜ˆì¸¡ê°’', 'ì˜¤ì°¨']])
    
    # CSV ì €ì¥
    df_results.to_csv('evaluation_results.csv', index=False, encoding='utf-8-sig')
    print("\nâœ… ì €ì¥: evaluation_results.csv")
    
    return df_results

