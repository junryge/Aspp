    print("\n" + "="*80)
    print("ğŸ“Š í‰ê°€ í†µê³„")
    print("="*80)
    print(f"ì´ ì˜ˆì¸¡ ìˆ˜: {len(results_df)}")
    print(f"í‰ê·  ì˜¤ì°¨: {results_df['ì˜¤ì°¨'].abs().mean():.2f}")
