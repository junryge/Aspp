    print(f"  ë°œìƒ: {drop_count}ê°œ")
    print(f"  ì˜ˆì¸¡ ì„±ê³µ: {drop_detected}/{drop_count}ê°œ ({drop_detected/drop_count*100 if drop_count > 0 else 0:.1f}%)")
   
