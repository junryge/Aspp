    print(f"ì˜ˆì¸¡ê°’ ê·¹ë‹¨(â‰¥300): {(results_df['ì˜ˆì¸¡ê°’'] >= 300).sum()}ê°œ")
   
    # ìƒìœ„ ì˜¤ì°¨ êµ¬ê°„
    print("\n" + "="*80)
    print("âŒ ì˜¤ì°¨ ìƒìœ„ 10ê°œ êµ¬ê°„")
    print("="*80)
    top_errors = results_df.nlargest(10, 'ì˜¤ì°¨ìœ¨(%)')
