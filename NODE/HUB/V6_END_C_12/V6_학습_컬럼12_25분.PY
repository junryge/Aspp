def train_and_evaluate_complete():
    """
    í•™ìŠµ(aas.csv) â†’ í‰ê°€(BBB.CSV) ì „ì²´ í”„ë¡œì„¸ìŠ¤
    V4 Ultimate 21ê°œ í•„ìˆ˜ ì»¬ëŸ¼ í™œìš©
    """
    print("="*80)
    print("XGBoost 30ë¶„â†’25ë¶„ ì˜ˆì¸¡ ëª¨ë¸ í•™ìŠµ ë° í‰ê°€ (í•˜ì´í¼íŒŒë¼ë¯¸í„° ê°•í™”)")
    print("="*80)
    
    # í•µì‹¬ 12ê°œ ì»¬ëŸ¼ë§Œ ì‚¬ìš© (ë¬¼ë¦¬ì ìœ¼ë¡œ ì¤‘ìš”í•œ ê²ƒë§Œ)
    FEATURE_COLS = {
        'storage': ['M16A_3F_STORAGE_UTIL'],  # ê·¹ë‹¨ê°’ í•µì‹¬ ì§€í‘œ
        'cmd': ['M16A_3F_CMD', 'M16A_6F_TO_HUB_CMD'],  # ì£¼ìš” CMD
        'inflow': ['M16A_6F_TO_HUB_JOB', 'M16A_2F_TO_HUB_JOB2', 'M14A_3F_TO_HUB_JOB2'],  # ì£¼ìš” ìœ ì…
        'outflow': ['M16A_3F_TO_M16A_6F_JOB', 'M16A_3F_TO_M16A_2F_JOB', 'M16A_3F_TO_M14A_3F_JOB'],  # ì£¼ìš” ìœ ì¶œ
        'maxcapa': ['M16A_6F_LFT_MAXCAPA', 'M16A_2F_LFT_MAXCAPA']  # ìš©ëŸ‰ ì œí•œ
    }
    
    TARGET_COL = 'CURRENT_M16A_3F_JOB_2'
    
    # Feature ìƒì„± í•¨ìˆ˜
    def create_features(df, start_idx=30):
        features_list = []
        labels = []
        seq_max_list = []
        seq_min_list = []
        indices = []
        
        for i in range(start_idx, len(df) - 25):  # 15 â†’ 25
            seq_target = df[TARGET_COL].iloc[i-30:i].values
            
            features = {
                # íƒ€ê²Ÿ ì»¬ëŸ¼ íŠ¹ì„±
                'target_mean': np.mean(seq_target),
                'target_std': np.std(seq_target),
                'target_last_5_mean': np.mean(seq_target[-5:]),
                'target_max': np.max(seq_target),
                'target_min': np.min(seq_target),
                'target_slope': np.polyfit(np.arange(30), seq_target, 1)[0],
                'target_last_10_mean': np.mean(seq_target[-10:]),
                'target_first_10_mean': np.mean(seq_target[:10]),
            }
            
            # ê° ì»¬ëŸ¼ ê·¸ë£¹ë³„ íŠ¹ì„± ì¶”ê°€
            for group_name, cols in FEATURE_COLS.items():
                for col in cols:
                    if col in df.columns:
                        seq_data = df[col].iloc[i-30:i].values
                        
                        # ê¸°ë³¸ í†µê³„
                        features[f'{col}_mean'] = np.mean(seq_data)
                        features[f'{col}_std'] = np.std(seq_data)
                        features[f'{col}_max'] = np.max(seq_data)
                        features[f'{col}_min'] = np.min(seq_data)
                        
                        # ìµœê·¼ íŠ¹ì„±
                        features[f'{col}_last_5_mean'] = np.mean(seq_data[-5:])
                        features[f'{col}_last_10_mean'] = np.mean(seq_data[-10:])
                        
                        # ì¶”ì„¸
                        features[f'{col}_slope'] = np.polyfit(np.arange(30), seq_data, 1)[0]
                        
                        # êµ¬ê°„ë³„ í‰ê·  (ì´ˆê¸°/ì¤‘ê°„/ìµœê·¼)
                        features[f'{col}_first_10_mean'] = np.mean(seq_data[:10])
                        features[f'{col}_mid_10_mean'] = np.mean(seq_data[10:20])
                        features[f'{col}_last_value'] = seq_data[-1]
            
            # ìœ ì…-ìœ ì¶œ ì°¨ì´ (Net Flow)
            inflow_sum = 0
            outflow_sum = 0
            for col in FEATURE_COLS['inflow']:
                if col in df.columns:
                    inflow_sum += df[col].iloc[i-1]
            for col in FEATURE_COLS['outflow']:
                if col in df.columns:
                    outflow_sum += df[col].iloc[i-1]
            features['net_flow'] = inflow_sum - outflow_sum
            
            # CMD ì´í•©
            cmd_sum = 0
            for col in FEATURE_COLS['cmd']:
                if col in df.columns:
                    cmd_sum += df[col].iloc[i-1]
            features['total_cmd'] = cmd_sum
            
            features_list.append(features)
            labels.append(df[TARGET_COL].iloc[i:i+25].max())  # 15 â†’ 25 (MAX ìœ ì§€)
            seq_max_list.append(np.max(seq_target))
            seq_min_list.append(np.min(seq_target))
            indices.append(i)
        
        return pd.DataFrame(features_list), np.array(labels), seq_max_list, seq_min_list, indices
    
    # ===== 1. í•™ìŠµ ë‹¨ê³„ (aas.csv) =====
    print("\n[STEP 1] aas.csvë¡œ ëª¨ë¸ í•™ìŠµ")
    print("-"*40)
    
    df_train = pd.read_csv('HUB_0509_TO_0929_DATA.csv', on_bad_lines='skip')
    
    print(f"í•™ìŠµ ë°ì´í„°: {len(df_train)}ê°œ í–‰")
    print(f"ì‚¬ìš© ê°€ëŠ¥í•œ ì»¬ëŸ¼ í™•ì¸:")
    
    all_feature_cols = []
    for group_name, cols in FEATURE_COLS.items():
        available = [col for col in cols if col in df_train.columns]
        all_feature_cols.extend(available)
        print(f"  - {group_name}: {len(available)}/{len(cols)}ê°œ")
    
    # í•™ìŠµ ë°ì´í„° ìƒì„±
    X_train, y_train, _, _, _ = create_features(df_train)
    
    print(f"\nìƒì„±ëœ Feature ìˆ˜: {len(X_train.columns)}ê°œ")
    print(f"í•™ìŠµ ìƒ˜í”Œ ìˆ˜: {len(X_train)}ê°œ")
    
    # í•™ìŠµ/ê²€ì¦ ë¶„í• 
    X_tr, X_val, y_tr, y_val = train_test_split(
        X_train, y_train, test_size=0.2, random_state=42
    )
    
    # GPU/CPU ìë™ ì„ íƒ (ì‹¤ì œ í•™ìŠµìœ¼ë¡œ í…ŒìŠ¤íŠ¸)
    print("\nğŸ” í•™ìŠµ í™˜ê²½ ê°ì§€ ì¤‘...")
    use_gpu = False
    
    # 1ì°¨ ì‹œë„: GPU
    try:
        print("  â†’ GPU ëª¨ë“œ ì‹œë„ ì¤‘...")
        test_model = xgb.XGBRegressor(
            n_estimators=5,
            max_depth=3,
            random_state=42,
            tree_method='gpu_hist',
            gpu_id=0
        )
        # ì‹¤ì œë¡œ í•™ìŠµ ì‹œë„ (ì‘ì€ ìƒ˜í”Œë¡œ í…ŒìŠ¤íŠ¸)
        test_model.fit(X_tr[:100], y_tr[:100], verbose=False)
        use_gpu = True
        print("  âœ… GPU ì‚¬ìš© ê°€ëŠ¥! GPU ëª¨ë“œë¡œ í•™ìŠµí•©ë‹ˆë‹¤.\n")
    except Exception as e:
        print(f"  âš ï¸ GPU ì‚¬ìš© ë¶ˆê°€: {str(e)[:100]}")
        print("  â†’ CPU ëª¨ë“œë¡œ ì „í™˜í•©ë‹ˆë‹¤.\n")
        use_gpu = False
    
    # ì‹¤ì œ ëª¨ë¸ ìƒì„± (25ë¶„ ì˜ˆì¸¡ - n_estimators=100 + ê·¹ë‹¨ì  ì•½í™”)
    if use_gpu:
        model = xgb.XGBRegressor(
            n_estimators=100,           # 160 â†’ 100 (10ë¶„ì˜ 1/3 ìˆ˜ì¤€!)
            max_depth=3,                # 4 â†’ 3 (ë§¤ìš° ì–•ê²Œ)
            learning_rate=0.15,         # 0.1 â†’ 0.15 (í•™ìŠµë¥  3ë°°, ë§¤ìš° ê±°ì¹ ê²Œ)
            subsample=0.5,              # 0.6 â†’ 0.5 (ìƒ˜í”Œ ì ˆë°˜ë§Œ)
            colsample_bytree=0.5,       # 0.6 â†’ 0.5 (í”¼ì²˜ ì ˆë°˜ë§Œ)
            min_child_weight=10,        # 8 â†’ 10 (ë¦¬í”„ ë…¸ë“œ ì œì•½ ìµœëŒ€)
            gamma=0.7,                  # 0.5 â†’ 0.7 (ë¶„í•  ê±°ì˜ ë¶ˆê°€ëŠ¥í•˜ê²Œ)
            reg_alpha=0.8,              # 0.6 â†’ 0.8 (L1 ì •ê·œí™” ìµœëŒ€)
            reg_lambda=3.0,             # 2.5 â†’ 3.0 (L2 ì •ê·œí™” ìµœëŒ€)
            random_state=42,
            tree_method='gpu_hist',
            gpu_id=0,
            predictor='gpu_predictor'
        )
    else:
        model = xgb.XGBRegressor(
            n_estimators=100,           # 160 â†’ 100 (10ë¶„ì˜ 1/3 ìˆ˜ì¤€!)
            max_depth=3,                # 4 â†’ 3 (ë§¤ìš° ì–•ê²Œ)
            learning_rate=0.15,         # 0.1 â†’ 0.15 (í•™ìŠµë¥  3ë°°, ë§¤ìš° ê±°ì¹ ê²Œ)
            subsample=0.5,              # 0.6 â†’ 0.5 (ìƒ˜í”Œ ì ˆë°˜ë§Œ)
            colsample_bytree=0.5,       # 0.6 â†’ 0.5 (í”¼ì²˜ ì ˆë°˜ë§Œ)
            min_child_weight=10,        # 8 â†’ 10 (ë¦¬í”„ ë…¸ë“œ ì œì•½ ìµœëŒ€)
            gamma=0.7,                  # 0.5 â†’ 0.7 (ë¶„í•  ê±°ì˜ ë¶ˆê°€ëŠ¥í•˜ê²Œ)
            reg_alpha=0.8,              # 0.6 â†’ 0.8 (L1 ì •ê·œí™” ìµœëŒ€)
            reg_lambda=3.0,             # 2.5 â†’ 3.0 (L2 ì •ê·œí™” ìµœëŒ€)
            random_state=42,
            tree_method='hist',
            n_jobs=-1
        )
        
    print("ëª¨ë¸ í•™ìŠµ ì¤‘ (25ë¶„ ì˜ˆì¸¡ - ê·¹ë‹¨ì  ì•½í™”)...")
    model.fit(
        X_tr, y_tr,
        eval_set=[(X_val, y_val)],
        verbose=False
    )
    
    
    # í•™ìŠµ ë°ì´í„° í‰ê°€
    y_val_pred = model.predict(X_val)
    train_mae = mean_absolute_error(y_val, y_val_pred)
    train_rmse = np.sqrt(mean_squared_error(y_val, y_val_pred))
    train_r2 = r2_score(y_val, y_val_pred)
    
    print(f"\ní•™ìŠµ ë°ì´í„° ì„±ëŠ¥:")
    print(f"  MAE:  {train_mae:.4f}")
    print(f"  RMSE: {train_rmse:.4f}")
    print(f"  RÂ²:   {train_r2:.4f}")
    
    # ëª¨ë¸ ì €ì¥
    with open('xgboost_model_30min_25min_enhanced.pkl', 'wb') as f:
        pickle.dump(model, f)
    print("âœ… ëª¨ë¸ ì €ì¥ ì™„ë£Œ: xgboost_model_30min_25min_enhanced.pkl")
    
    # ===== 2. í‰ê°€ ë‹¨ê³„ (BBB.CSV) =====
    print("\n[STEP 2] BBB.CSVë¡œ ëª¨ë¸ í‰ê°€")
    print("-"*40)
    
    df_test = pd.read_csv('HUB_20250916_to_20250929.CSV', on_bad_lines='skip')
    
    print(f"í‰ê°€ ë°ì´í„°: {len(df_test)}ê°œ í–‰")
    
    # BBB.CSV Feature ìƒì„±
    X_test, y_test, seq_max_list, seq_min_list, indices = create_features(df_test)
    
    # ì˜ˆì¸¡
    y_pred = model.predict(X_test)
    
    # í‰ê°€ ì§€í‘œ
    test_mae = mean_absolute_error(y_test, y_pred)
    test_rmse = np.sqrt(mean_squared_error(y_test, y_pred))
    test_r2 = r2_score(y_test, y_pred)
    
    print(f"\nBBB.CSV í‰ê°€ ê²°ê³¼:")
    print(f"  MAE:  {test_mae:.4f}")
    print(f"  RMSE: {test_rmse:.4f}")
    print(f"  RÂ²:   {test_r2:.4f}")
    
    # ===== 3. ìƒì„¸ ë¶„ì„ ê²°ê³¼ ìƒì„± =====
    print("\n[STEP 3] ìƒì„¸ ë¶„ì„ ê²°ê³¼ ìƒì„±")
    print("-"*40)
    
    # STAT_DT ì²˜ë¦¬
    if 'STAT_DT' in df_test.columns:
        try:
            df_test['STAT_DT'] = pd.to_datetime(df_test['STAT_DT'], format='%Y%m%d%H%M')
        except:
            try:
                df_test['STAT_DT'] = pd.to_datetime(df_test['STAT_DT'])
            except:
                base_date = datetime(2024, 1, 1)
                df_test['STAT_DT'] = [base_date + timedelta(minutes=i) for i in range(len(df_test))]
    else:
        base_date = datetime(2024, 1, 1)
        df_test['STAT_DT'] = [base_date + timedelta(minutes=i) for i in range(len(df_test))]
    
    # ê²°ê³¼ DataFrame ìƒì„±
    results = []
    jump_count = 0
    extreme_count = 0
    extreme_detected = 0
    
    for i, idx in enumerate(indices):
        current_time = df_test['STAT_DT'].iloc[idx]
        seq_start_time = df_test['STAT_DT'].iloc[idx-30]
        prediction_time = current_time + timedelta(minutes=25)  # 15 â†’ 25
        
        is_jump = (seq_max_list[i] < 280) and (y_test[i] >= 300)
        if is_jump:
            jump_count += 1
        
        if y_test[i] >= 300:
            extreme_count += 1
            if y_pred[i] >= 290:
                extreme_detected += 1
        
        results.append({
            'í˜„ì¬ì‹œê°„': current_time.strftime('%Y-%m-%d %H:%M'),
            'ì˜ˆì¸¡ì‹œê°„(+25ë¶„)': prediction_time.strftime('%Y-%m-%d %H:%M'),  # +15ë¶„ â†’ +25ë¶„
            'ì‹œí€€ìŠ¤ì‹œì‘': seq_start_time.strftime('%Y-%m-%d %H:%M'),
            'ì‹œí€€ìŠ¤ì™„ë£Œ': current_time.strftime('%Y-%m-%d %H:%M'),
            'ì‹¤ì œê°’': round(y_test[i], 2),
            'ì˜ˆì¸¡ê°’': round(y_pred[i], 2),
            'ì˜¤ì°¨': round(abs(y_test[i] - y_pred[i]), 2),
            'ì˜¤ì°¨ìœ¨(%)': round(abs(y_test[i] - y_pred[i]) / y_test[i] * 100, 2),
            'ì‹œí€€ìŠ¤MAX': round(seq_max_list[i], 2),
            'ì‹œí€€ìŠ¤MIN': round(seq_min_list[i], 2),
            'ì‹œí€€ìŠ¤ë²”ìœ„': round(seq_max_list[i] - seq_min_list[i], 2),
            'ì í”„ì¼€ì´ìŠ¤': 'O' if is_jump else '-',
            'ê·¹ë‹¨ê°’(300+)': 'O' if y_test[i] >= 300 else '-',
            'ê·¹ë‹¨ê°’ê°ì§€': 'O' if (y_test[i] >= 300 and y_pred[i] >= 290) else '-'
        })
    
    df_results = pd.DataFrame(results)
    
    # í†µê³„ ì¶œë ¥
    print(f"\nğŸ“Š íŠ¹ìˆ˜ ì¼€ì´ìŠ¤ ë¶„ì„:")
    print(f"  - ì „ì²´ ì˜ˆì¸¡: {len(df_results)}ê°œ")
    print(f"  - ê·¹ë‹¨ê°’(300+): {extreme_count}ê°œ ({extreme_count/len(df_results)*100:.1f}%)")
    print(f"  - ê·¹ë‹¨ê°’ ê°ì§€: {extreme_detected}/{extreme_count}ê°œ ({extreme_detected/extreme_count*100 if extreme_count > 0 else 0:.1f}%)")
    print(f"  - ì í”„ ì¼€ì´ìŠ¤: {jump_count}ê°œ ({jump_count/len(df_results)*100:.1f}%)")
    
    # ì í”„ ì¼€ì´ìŠ¤ ìƒì„¸ ì¶œë ¥
    if jump_count > 0:
        jump_df = df_results[df_results['ì í”„ì¼€ì´ìŠ¤'] == 'O']
        print(f"\nğŸ”¥ ì í”„ ì¼€ì´ìŠ¤ ìƒì„¸ (ì‹œí€€ìŠ¤MAX<280 â†’ ì‹¤ì œê°’â‰¥300):")
        display_cols = ['í˜„ì¬ì‹œê°„', 'ì‹œí€€ìŠ¤ì‹œì‘', 'ì‹œí€€ìŠ¤ì™„ë£Œ', 'ì‹œí€€ìŠ¤MAX', 'ì‹¤ì œê°’', 'ì˜ˆì¸¡ê°’', 'ì˜¤ì°¨']
        print(jump_df[display_cols].head(5).to_string())
    
    # ê·¹ë‹¨ê°’ ì¼€ì´ìŠ¤ ìƒì„¸ ì¶œë ¥
    if extreme_count > 0:
        extreme_df = df_results[df_results['ê·¹ë‹¨ê°’(300+)'] == 'O']
        print(f"\nâš ï¸ ê·¹ë‹¨ê°’ ì¼€ì´ìŠ¤ ìƒì„¸ (ì‹¤ì œê°’ â‰¥ 300):")
        display_cols = ['í˜„ì¬ì‹œê°„', 'ì‹œí€€ìŠ¤ì™„ë£Œ', 'ì‹¤ì œê°’', 'ì˜ˆì¸¡ê°’', 'ì˜¤ì°¨', 'ê·¹ë‹¨ê°’ê°ì§€']
        print(extreme_df[display_cols].head(5).to_string())
    
    # CSV ì €ì¥
    df_results.to_csv('BBB_evaluation_results_25min.csv', index=False, encoding='utf-8-sig')
    print(f"\nâœ… ìƒì„¸ ê²°ê³¼ ì €ì¥: BBB_evaluation_results_25min.csv")
    
    # ===== 4. ê·¸ë˜í”„ ìƒì„± =====
    print("\n[STEP 4] í‰ê°€ ê·¸ë˜í”„ ìƒì„±")
    print("-"*40)
    
    fig, axes = plt.subplots(2, 3, figsize=(18, 10))
    
    # 1. ì˜ˆì¸¡ vs ì‹¤ì œ ì‚°ì ë„
    ax1 = axes[0, 0]
    ax1.scatter(y_test, y_pred, alpha=0.5, s=10)
    ax1.plot([y_test.min(), y_test.max()], [y_test.min(), y_test.max()], 'r--', lw=2)
    ax1.set_xlabel('Actual')
    ax1.set_ylabel('Predicted')
    ax1.set_title(f'Actual vs Predicted (25min)\nMAE={test_mae:.2f}, RÂ²={test_r2:.3f}')
    ax1.grid(True, alpha=0.3)
    
    # 2. ì‹œê³„ì—´ ë¹„êµ
    ax2 = axes[0, 1]
    plot_size = min(300, len(y_test))
    ax2.plot(range(plot_size), y_test[:plot_size], 'b-', label='Actual', alpha=0.7, linewidth=1)
    ax2.plot(range(plot_size), y_pred[:plot_size], 'r--', label='Predicted', alpha=0.7, linewidth=1)
    ax2.axhline(y=300, color='orange', linestyle='--', label='Extreme(300)', alpha=0.5)
    ax2.set_xlabel('Time Index')
    ax2.set_ylabel('Value')
    ax2.set_title('Time Series Comparison (First 300)')
    ax2.legend()
    ax2.grid(True, alpha=0.3)
    
    # 3. ì˜¤ì°¨ ë¶„í¬
    ax3 = axes[0, 2]
    errors = y_pred - y_test
    ax3.hist(errors, bins=50, edgecolor='black', alpha=0.7, color='skyblue')
    ax3.axvline(x=0, color='r', linestyle='--', linewidth=2)
    ax3.set_xlabel('Prediction Error')
    ax3.set_ylabel('Frequency')
    ax3.set_title(f'Error Distribution\nMean={np.mean(errors):.2f}, Std={np.std(errors):.2f}')
    ax3.grid(True, alpha=0.3)
    
    # 4. ê·¹ë‹¨ê°’ ì„±ëŠ¥
    ax4 = axes[1, 0]
    extreme_mask = y_test >= 300
    if extreme_mask.any():
        ax4.scatter(y_test[~extreme_mask], y_pred[~extreme_mask], 
                   alpha=0.3, s=5, label='Normal', color='blue')
        ax4.scatter(y_test[extreme_mask], y_pred[extreme_mask], 
                   alpha=0.8, s=20, label='Extreme(300+)', color='red')
        ax4.plot([200, 500], [200, 500], 'k--', lw=1)
        ax4.axhline(y=300, color='orange', linestyle='--', alpha=0.5)
        ax4.axvline(x=300, color='orange', linestyle='--', alpha=0.5)
        ax4.set_xlabel('Actual')
        ax4.set_ylabel('Predicted')
        ax4.set_title(f'Extreme Value Performance\nDetected: {extreme_detected}/{extreme_count}')
        ax4.legend()
        ax4.grid(True, alpha=0.3)
    
    # 5. ì í”„ ì¼€ì´ìŠ¤ ë¶„ì„
    ax5 = axes[1, 1]
    if jump_count > 0:
        jump_mask = df_results['ì í”„ì¼€ì´ìŠ¤'] == 'O'
        jump_actual = df_results[jump_mask]['ì‹¤ì œê°’'].values
        jump_pred = df_results[jump_mask]['ì˜ˆì¸¡ê°’'].values
        jump_seq_max = df_results[jump_mask]['ì‹œí€€ìŠ¤MAX'].values
        
        ax5.scatter(jump_seq_max, jump_actual, label='Actual Jump', s=50, alpha=0.8, color='red')
        ax5.scatter(jump_seq_max, jump_pred, label='Predicted', s=30, alpha=0.6, color='blue')
        
        for i in range(len(jump_seq_max)):
            ax5.plot([jump_seq_max[i], jump_seq_max[i]], 
                    [jump_pred[i], jump_actual[i]], 
                    'gray', alpha=0.3, linewidth=0.5)
        
        ax5.axhline(y=300, color='orange', linestyle='--', label='Threshold')
        ax5.axvline(x=280, color='green', linestyle='--', alpha=0.5, label='SeqMax=280')
        ax5.set_xlabel('Sequence MAX')
