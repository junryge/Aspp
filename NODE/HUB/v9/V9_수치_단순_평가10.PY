    print(f"  - í‰ê· : {results_df['ì‹¤ì œë²”ìœ„í­'].mean():.1f}")
    print(f"  - ì¤‘ì•™ê°’: {results_df['ì‹¤ì œë²”ìœ„í­'].median():.1f}")
    
