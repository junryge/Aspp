        actual_min = np.min(future_10min)
        actual_max = np.max(future_10min)
        actual_change = actual_value - current_value
        
        # ğŸ”¥ Feature ìƒì„± (ë‹¨ìˆœí™” - í•µì‹¬ë§Œ)
