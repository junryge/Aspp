    output_file = 'ìˆ˜ì¹˜í˜•_V9_Simple_í‰ê°€ê²°ê³¼.csv'
    results_df.to_csv(output_file, index=False, encoding='utf-8-sig')
    print(f"\nâœ… ê²°ê³¼ ì €ì¥: {output_file}")
    
    # í†µê³„
    print("\n" + "="*80)
    print("ğŸ“Š í‰ê°€ í†µê³„ (V9-ìˆ˜ì¹˜í˜• ë‹¨ìˆœí™”)")
    print("="*80)
    
