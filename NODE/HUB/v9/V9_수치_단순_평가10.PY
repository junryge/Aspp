    print(f"  - ìµœì†Œ: {results_df['ì˜ˆì¸¡ë²”ìœ„í­'].min():.1f}")
    print(f"  - ìµœëŒ€: {results_df['ì˜ˆì¸¡ë²”ìœ„í­'].max():.1f}")
    
