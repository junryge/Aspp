def get_alert_level(m14b, m14bsum, gap, totalcnt):
    """íŒ¨í„´ â†’ ê²½ë³´ ë ˆë²¨ë§Œ! ì˜ˆì¸¡ê°’ì— ì˜í–¥ ì—†ìŒ!"""
    
    if m14b > 520 and m14bsum > 600 and gap > 400:
        return 'CRITICAL'
    if gap > 500:
        return 'CRITICAL'
    if m14b > 520 and m14bsum > 600 and gap > 250:
        return 'CRITICAL'
    if gap > 400:
        return 'HIGH'
    if m14b > 520 and m14bsum > 600:
        return 'HIGH'
    if totalcnt >= 1680:
        return 'HIGH'
    if totalcnt >= 1650:
        return 'WARNING'
    if totalcnt >= 1600:
        return 'CAUTION'
    return 'NORMAL'

